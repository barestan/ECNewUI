<script>
    import axios from "axios";

    export default {
        name: "ErrorsTableItem",
        data: function () {
            return {
                collapsed: true,
                countryInfo: ({}),
                htmlStack: ""
            }
        },
        props: {
            item: { type: Object, default: () => ({})},
            log: { type: Object, default: () => ({})},
            id: { type: String, default: () => ("")},
            mode: {type: String, default: () => ("as-table")}
        },
        computed: {
            listBox: function () {
                return document.getElementById("items-container");
            },
            elmah_root: function () {
                return window.$elmah_root;
            },
        },
        methods:{
            levelString: function (level) {
                switch (level) {
                    case 0 : return "Trace";
                    case 1 : return "Debug";
                    case 2 : return "Info";
                    case 3 : return "Warning";
                    case 4 : return "Error";
                    case 5 : return "Critical";
                    default : return "None";
                }
            },
            copyTextToClipboard: function copyTextToClipboard() {
                let bvToast = this.$bvToast;

                let text =
`URL: (${this.item.method}) ${this.item.url}
Hostname: ${this.item.hostName}
Status Code: ${this.item.statusCode}
Type: ${this.item.type}
Message: ${this.item.message}
Source: ${this.item.source}
User: ${this.item.user}
Client IP: ${this.item.client}
Time: ${this.item.time}
Detail:
${this.item.detail}
`
                if (this.item.header && Object.keys(this.item.header).length !== 0) {
                    text += '\nHeader:\n'; let item = this.item;
                    text += Object.keys(this.item.header).map(function (k) {return "\t" + k + ":" + item.header[k]}).join("\n")
                }

                if (this.item.cookies && Object.keys(this.item.cookies).length !== 0) {
                    text += '\nCookies:\n'; let item = this.item;
                    text += Object.keys(this.item.cookies).map(function (k) {return "\t" + k + ":" + item.cookies[k]}).join("\n")
                }

                if (this.item.form && Object.keys(this.item.form).length !== 0) {
                    text += '\nForm:\n'; let item = this.item;
                    text += Object.keys(this.item.form).map(function (k) {return "\t" + k + ":" + item.form[k]}).join("\n")
                }

                if (this.item.connection && Object.keys(this.item.connection).length !== 0) {
                    text += '\nConnection:\n'; let item = this.item;
                    text += Object.keys(this.item.connection).map(function (k) {return "\t" + k + ":" + item.connection[k]}).join("\n")
                }

                if (this.item.userData && Object.keys(this.item.userData).length !== 0) {
                    text += '\nUser Data:\n'; let item = this.item;
                    text += Object.keys(this.item.userData).map(function (k) {return "\t" + k + ":" + item.userData[k]}).join("\n")
                }

                if (this.item.session && Object.keys(this.item.session).length !== 0) {
                    text += '\nSession:\n'; let item = this.item;
                    text += Object.keys(this.item.session).map(function (k) {return "\t" + k + ":" + item.session[k]}).join("\n")
                }

                if (this.item.serverVariables && Object.keys(this.item.serverVariables).length !== 0) {
                    text += '\nServer Variables:\n'; let item = this.item;
                    text += Object.keys(this.item.serverVariables).map(function (k) {return "\t" + k + ":" + item.serverVariables[k]}).join("\n")
                }
                function fallbackCopyTextToClipboard(text) {
                    var textArea = document.createElement("textarea");
                    textArea.value = text;

                    // Avoid scrolling to bottom
                    textArea.style.top = "0";
                    textArea.style.left = "0";
                    textArea.style.position = "fixed";

                    document.body.appendChild(textArea);
                    textArea.focus();
                    textArea.select();

                    try {
                        document.execCommand('copy');
                        bvToast.toast('Error copied to clipboard.', {
                            variant: 'success',
                            solid: true,
                            noCloseButton: true,
                            autoHideDelay: 2000
                        });
                    } catch (err) {
                        console.error('Fallback: Oops, unable to copy', err);
                    }

                    document.body.removeChild(textArea);
                }

                if (!navigator.clipboard) {
                    fallbackCopyTextToClipboard(text);
                    return;
                }
                navigator.clipboard.writeText(text).then(function() {
                    bvToast.toast('Error copied to clipboard.', {
                            variant: 'success',
                            solid: true,
                            noCloseButton: true,
                            autoHideDelay: 2000
                        });
                }, function(err) {
                    console.error('Async: Could not copy text: ', err);
                });
            }
        },
        watch:{
            collapsed: function (oldValue, newVaule) {
                if (!newVaule){
                    this.listBox.focus();
                }
                if (newVaule == true && (Object.keys(this.countryInfo).length === 0 && this.countryInfo.constructor === Object)){
                    if (this.item.client && !this.item.client.startsWith(":")) {
                        axios
                            .get('http://ip-api.com/json/' + this.item.client)
                            .then(response => (this.countryInfo = response.data))
                            .catch(e => console.log(e));
                    }

                }
                //Parse stack
                if (this.htmlStack != "") return;
                function getParams(line) {
                    if (line.length <= 1 ) return "";
                    let pars = line.split(',');
                    pars = pars.map(function (li) {
                        li = li.trim();
                        let sp = li.split(' ');
                        if (sp.length == 2) return "<span class='st-type'>" + sp[0]+" </span><span class='param'>"+sp[1]+"</span>";
                        return li;
                    });
                    return pars.join('<span class="separator">, </span>');
                }
                function getMethodWithParams(line) {
                    line = line.trimEnd();
                    let start = line.lastIndexOf('.'), end = line.indexOf('(',start), methodName = line.substring(start, end);
                    return line.substring(0,start) + "<span class='st-method'>" + methodName +
                        "</span><span class='separator'>(</span>"+ getParams(line.substring(end + 1, line.length - 2)) +
                        "<span class='separator'>)</span>";
                }
                if (this.item.detail) {
                    let lines = this.item.detail.split(/(?=(--- |at |in ))/).filter(function (element, index) {
                        return (index % 2 === 0);
                    })
                    lines = lines.map(function (line) {
                        if (line.startsWith('--- ')) return "<div class='line st-location'>---  <span class='st-location'>" + line.substring(3) + "</span> </div>";
                        if (line.startsWith('at ')) return "<div class='line st-frame'>at <span class='st-frame'>" + getMethodWithParams(line.substring(3)) + "</span> </div>";
                        if (line.startsWith('in ')) return "<div class='line st-source'>in <span class='st-source'>" + line.substring(3) + "</span> </div>";
                        return line;
                    });
                    this.htmlStack = lines.join(' ');
                }
            }
        }
    }
</script>
